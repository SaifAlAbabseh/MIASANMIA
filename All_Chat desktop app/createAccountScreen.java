    private void succCreateAcc(String username , String password){
        String db="jdbc:mysql://localhost/allchat";
        String user="root";
        String pass="12345678";
        
        try{
            Connection conn=DriverManager.getConnection(db,user,pass);
            Statement stmt=conn.createStatement();
            String query="SELECT * FROM users WHERE BINARY username='"+username+"'";
            ResultSet result=stmt.executeQuery(query);
            if(result.next()){
                JOptionPane.showMessageDialog(null,"Username is already used.","Invalid",JOptionPane.WARNING_MESSAGE);
            }
            else{
                String query2="INSERT INTO users VALUES ('"+username+"','"+password+"','defaultUser','0')";
                stmt.executeUpdate(query2);
                restart();
                JOptionPane.showMessageDialog(null,"Successfully created your account.","Successful",JOptionPane.PLAIN_MESSAGE);
            }
            stmt.close();
            conn.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,""+ex.getMessage(),"Error" ,JOptionPane.ERROR_MESSAGE);
        }
        
    }
    private void createAccount(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccount

        String username = jTextField1.getText();
        String password = jPasswordField1.getText();
        String cPassword = jPasswordField2.getText();

        if (checkIfNotEmpty(username, password, cPassword)) {
            if (checkConfirmPassword(password, cPassword)) {
                if(checkUsernameIfValid(username)){
                    if(checkPasswordIfValid(password)){
                        succCreateAcc(username, password);
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Check password requirements.","Invalid Input",JOptionPane.ERROR_MESSAGE);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null,"Check username requirements.","Invalid Input",JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Passwords doesn't match.", "Invalid Inputs", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty.", "Invalid Inputs", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createAccount

