    private boolean checkConfirmPassword(String pass, String cPass) {
        boolean temp=true;
        if(!(jPasswordField1.getForeground()==Color.black && jPasswordField2.getForeground()==Color.black)){
            temp=false;
        }
        if (pass.equals(cPass)) {
            if(temp){
                return true;
            }
        }
        else{
            temp=false;
        }
        return temp;
    }

    private boolean checkIfNotEmpty(String username, String pass, String cpass) {
        boolean temp=true;
        if(!(jTextField1.getForeground()==Color.black && jPasswordField1.getForeground()==Color.black && jPasswordField2.getForeground()==Color.black)){
            temp=false;
        }
        if (!(username.trim().equals("") && pass.trim().equals("") && cpass.trim().equals(""))) {
            if(temp){
                return true;
            }
        }
        return temp;
    }

    private boolean checkUsernameIfValid(String username) {
        int length = username.length();
        boolean temp = true;
        if (!(length >= 6 && length <= 12)) {
            temp = false;
        } else {
            for (int i = 0; i < length; i++) {
                if (!((username.charAt(i) >= 65 && username.charAt(i) <= 90) || (username.charAt(i) >= 97 && username.charAt(i) <= 122))) {
                    if (!(username.charAt(i) >= 48 && username.charAt(i) <= 57)) {
                        temp = false;
                        break;
                    }
                }
            }
        }
        return temp;
    }
    private boolean checkPasswordIfValid(String password){
        int length=password.length();
        boolean temp=true;
        if(!(length>=8 && length<=16)){
            temp=false;
        }
        else{
            for (int i = 0; i < length; i++) {
                if(password.charAt(i)== 32){
                    temp=false;
                    break;
                }
            }
        }
        return temp;
    }
